package JavaTest;

public class DoWhileLoop {

	public static void main(String[] args) {
		int i=1;
		for(i=1;i<=5;i++);
		{
			System.out.println("arsh");
		}
		while(i<=5);
		{
			System.out.println("arsh");
			i++;
		}
		do
		{
			System.out.println("arsh");
			i++;
		}
		while(i<=5);
		}

}
