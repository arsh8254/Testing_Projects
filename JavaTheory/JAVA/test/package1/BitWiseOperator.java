package test.package1;

public class BitWiseOperator {

	public static void main(String[] args) {
		int a=5;
		int b=7;
//		a= 0101 and b= 0111
//		a&b=0101 = 5  bitwise and
//		a|b=0111 = 7  bitwise or
				System.out.println("a & b = " + (a & b));
				System.out.println("a | b = " + (a | b));

	}

}
